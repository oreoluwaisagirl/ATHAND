// Get all workers
export const getAllWorkers = () => {
  return Object.values(workers).flat();
};

// Get workers by category
export const getWorkersByCategory = (categoryId) => {
  return workers[categoryId] || [];
};

// Get featured workers
export const getFeaturedWorkers = () => {
  const allWorkers = getAllWorkers();
  return allWorkers
    .sort((a, b) => b.rating - a.rating)
    .slice(0, 6)
    .map(worker => {
      // Find which category this worker belongs to
      const categoryId = Object.keys(workers).find(key => 
        workers[key].some(w => w.id === worker.id)
      ) || 'cleaner';
      return { ...worker, categoryId };
    });
};

// Get worker by ID
export const getWorkerById = (categoryId, workerId) => {
  const categoryWorkers = workers[categoryId] || [];
  return categoryWorkers.find((w) => w.id === workerId);
};

